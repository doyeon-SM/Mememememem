// 멤 시스템 전역 Enum 정의
// 
// [담당자 안내]
// - MemTier: 멤의 등급 (레어 ~ 신화). 등급별 스탯/출현 지역이 다릅니다.
// - MemPersonality: 멤의 성격. 플레이어와의 적대 조건을 결정합니다.
// - ProductionStatType: 영지 시설 배치 시 참조하는 생산 스탯 종류입니다.
// ============================================================================
namespace MemSystem.Data
{
    /// <summary>
    /// 멤 등급 (5단계).
    /// 등급이 높을수록 자원 생산 속도가 빠르며,
    /// 체력과 허기는 등급별로 고정된 값을 가집니다.
    /// 
    /// 기획서 참조: [2.2. 등급별 상세 스펙]
    /// - Rare: 기본 단계, 제한 없이 포획 가능
    /// - Epic: 1~3구역 출현
    /// - Unique: 2~4구역 출현
    /// - Legendary: 4구역(최상)
    /// - Mythic: 추후 밸런싱 예정
    /// </summary>
    public enum MemTier
    {
        Rare = 0,        // 레어 — 기본 단계
        Epic = 1,        // 에픽 — 1~3구역 출현
        Unique = 2,      // 유니크 — 2~4구역 출현
        Legendary = 3,   // 레전더리 — 4구역 출현
        Mythic = 4       // 신화 — 추후 상세 밸런싱 예정
    }

    /// <summary>
    /// 멤 성격 (적대 조건 결정).
    /// 
    /// 기획서 참조: [3. 성격(Aggro) 시스템]
    /// - Docile: 플레이어를 절대 공격하지 않음. 피격 시 도주.
    /// - Normal: 플레이어에게 피격당할 시 반격함.
    /// - Aggressive: 자신의 영역(인식 범위)에 플레이어가 침범하면 선제 공격함.
    /// </summary>
    public enum MemPersonality
    {
        Docile = 0,      // 온순 — 절대 공격 안 함, 피격 시 도주
        Normal = 1,      // 평범 — 피격 시 반격, HP 낮으면 도주
        Aggressive = 2   // 난폭 — 영역 침범 시 선제공격, HP 낮으면 도주
    }

    /// <summary>
    /// 생산 스탯 종류 (5종).
    /// 영지 시설 배치 시 해당 스탯이 최소 1단계 이상이어야 투입 가능합니다.
    /// 
    /// 기획서 참조: [2.1. 생산 스탯 (5종)]
    /// </summary>
    public enum ProductionStatType
    {
        Crafting = 0,    // 제작 — 제작대, 주방 배치 (제작 속도 증가)
        Logging = 1,     // 벌목 — 벌목장 배치 (원목 생산량 증가)
        Mining = 2,      // 채광 — 채굴장 배치 (원석 생산량 증가)
        Transport = 3,   // 이동 — 운반 시설, 발전기 배치 (회수 시설 수 및 전력 생산량 증가)
        Farming = 4      // 생산 — 밭, 목장 배치 (재료 생산량 증가)
    }
}
